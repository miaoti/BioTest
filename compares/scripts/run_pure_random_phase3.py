def _run_mutmut_cell(
    sut: str,
    corpus_dir: Path,
    budget_s: int,
    out_dir: Path,
    src_path_finder,  # Callable[[], Path]
    runner_script: Path,
    src_env_var: str,
    mutate_dir_name: str,
    include_files: tuple[str, ...] | None = None,
    corpus_sample: int = 200,
) -> dict:
    """Shared mutmut cell for vcfpy / biopython.

    `include_files` enforces the biotest_config.target_filters scope
    at copy time — only files BioTest actually exercises get mutated,
    so the score denominator isn't padded with unreachable mutants in
    off-path helpers (bgzf, tabix, etc.)."""
    out_dir.mkdir(parents=True, exist_ok=True)
    baseline_file = out_dir / "baseline.json"
    runner_log = out_dir / "runner.log"
    mutmut_log = out_dir / "mutmut_run.log"
    results_txt = out_dir / "mutmut_results.txt"
    summary_path = out_dir / "summary.json"

    # Materialise a mutable copy of the SUT source under `src/`. We
    # copy the WHOLE package so the import graph stays intact (vcfpy's
    # __init__.py pulls from vcfpy.exceptions / vcfpy.bgzf), but we
    # will restrict `--paths-to-mutate` to the in-scope files below so
    # mutmut only injects mutations into what BioTest exercises
    # (DESIGN §3.3 Scoping; Flow.md vcfpy row "排除 bgzf/tabix").
    src_src = src_path_finder()
    src_dst = _copy_source_tree(
        src_src, out_dir / "src" / mutate_dir_name,
        include_files=None,
    )

    # 1. Capture baseline fingerprints from the unmutated SUT.
    logger.info("[%s] capturing baseline → %s", sut, baseline_file)
    env = os.environ.copy()
    env.update({
        "MUTMUT_RUNNER_MODE": "baseline",
        src_env_var: str(src_dst.resolve()),
        "MUTMUT_CORPUS_DIR": str(corpus_dir.resolve()),
        "MUTMUT_BASELINE_FILE": str(baseline_file.resolve()),
        "MUTMUT_CORPUS_SAMPLE": str(corpus_sample),
        "MUTMUT_CORPUS_TIMEOUT_S": "2.0",
        "PYTHONUNBUFFERED": "1",
    })
    with runner_log.open("wb") as fh:
        rc = subprocess.run(
            [sys.executable, str(runner_script)],
            env=env, stdout=fh, stderr=fh,
            check=False, timeout=600,
        ).returncode
    if rc != 0 or not baseline_file.exists():
        return {
            "skipped": True,
            "blocked_reason": (
                f"baseline capture failed for {sut} (rc={rc}); "
                f"see {runner_log}"
            ),
        }
    baseline_entries = len(json.loads(
        baseline_file.read_text(encoding="utf-8"),
    ))
    logger.info("[%s] baseline entries: %d", sut, baseline_entries)

    # 2. mutmut 2.5 refuses to start unless a `tests/` dir exists (even
    #    though we pass a custom --runner that doesn't use pytest). Put
    #    a marker file so the directory scan succeeds; the runner ignores
    #    anything in tests/.
    (out_dir / "tests").mkdir(exist_ok=True)
    (out_dir / "tests" / ".keep").touch()

    # 3. Invoke `mutmut run` with CLI flags (mutmut 2.5.1 syntax).
    #    `--paths-to-mutate` is the BioTest-exercised scope — a
    #    comma-separated list of files if `include_files` is set, or
    #    the whole package directory otherwise. `--runner` is the
    #    per-mutant test suite (our fingerprint-compare driver).
    if include_files:
        # Scope to individual files: mutmut mutates only these.
        target_rel = ",".join(
            f"src/{mutate_dir_name}/{name}"
            for name in include_files
            if name != "__init__.py"  # don't mutate the package marker
        )
    else:
        target_rel = f"src/{mutate_dir_name}"
    # mutmut forwards `--runner` to shell execution, so it must be a
    # single string; use forward-slashes for Windows friendliness.
    runner_cmd = (
        f'"{sys.executable}" "{runner_script.resolve()}"'
    )
    env["MUTMUT_RUNNER_MODE"] = "check"
    # mutmut's progress renderer uses Braille spinner chars that cp1252
    # can't encode; force UTF-8 on the subprocess so the ASCII fallback
    # doesn't kick in mid-run with a UnicodeEncodeError.
    env["PYTHONIOENCODING"] = "utf-8"
    # Clean any prior .mutmut-cache so runs are deterministic (mutmut
    # 2.5.1 keeps cache in cwd; would otherwise resume partially-run
    # cells across script invocations). mutmut 2.5's cache is a single
    # SQLite file (not a directory) so use unlink().
    cache_file = out_dir / ".mutmut-cache"
    if cache_file.exists():
        try:
            if cache_file.is_file():
                cache_file.unlink()
            else:
                shutil.rmtree(cache_file, ignore_errors=True)
        except (OSError, PermissionError) as e:
            logger.warning(
                "[%s] could not clear .mutmut-cache (%s) — if this is a "
                "zombie lock, rename --out to a fresh dir for this run",
                sut, e,
            )
    start = time.time()
    with mutmut_log.open("wb") as fh:
        try:
            rc = subprocess.run(
                [
                    sys.executable, "-m", "mutmut", "run",
                    "--paths-to-mutate", target_rel,
                    "--runner", runner_cmd,
                    # `--CI` uniform exit codes; easier to parse.
                    "--CI",
                    # Quieter output — no Braille spinner.
                    "--simple-output",
                ],
                env=env, cwd=str(out_dir), stdout=fh, stderr=fh,
                check=False, timeout=budget_s,
            ).returncode
        except subprocess.TimeoutExpired:
            rc = -1
    dur = time.time() - start
    logger.info("[%s] mutmut run rc=%s in %.0fs", sut, rc, dur)

    # 4. `mutmut results` summary.
    with results_txt.open("wb") as fh:
        subprocess.run(
            [sys.executable, "-m", "mutmut", "results"],
            env=env, cwd=str(out_dir), stdout=fh, stderr=fh,
            check=False, timeout=120,
        )

    # 5. mutmut 2.5's `.mutmut-cache` is a SQLite file (not a directory).
    #    Back it up with copy2 for audit; leave original in place so
    #    `mutmut results` can still read it on a re-run. The cache is
    #    the authoritative source for killed/survived/suspicious counts
    #    (mutmut 2.5.1's text-mode `results` output hides the "killed"
    #    section — see _sqlite_status_counts below for the fix).
    cache_db = out_dir / ".mutmut-cache"
    cache_backup = out_dir / "mutmut_cache_backup.sqlite"
    if cache_db.exists() and cache_db.is_file():
        shutil.copy2(cache_db, cache_backup)

    # Prefer the SQLite cache (authoritative) over `mutmut results`
    # text (2.5.1 hides the "killed" section). Fall through to the
    # text parser if cache is missing.
    stats = _sqlite_status_counts(cache_backup)
    if stats is None:
        stats = _summarise_mutmut_output(results_txt, mutmut_log)
    stats.update({
        "tool": "pure_random",
        "sut": sut,
        "phase": "mutation",
        "engine": "mutmut 2.5",
        "time_budget_s": budget_s,
        "mutmut_exit_code": rc,
        "mutmut_duration_s": round(dur, 2),
        "baseline_entries": baseline_entries,
        "paths_to_mutate": [target_rel],
        "corpus_dir": str(corpus_dir),
    })
    summary_path.write_text(json.dumps(stats, indent=2), encoding="utf-8")
    logger.info("[%s] wrote %s  killed=%d survived=%d score=%s",
                sut, summary_path, stats.get("killed", 0),
                stats.get("survived", 0), stats.get("score_display"))
    return stats


def _sqlite_status_counts(cache_sqlite: Path) -> dict | None:
    """Read mutant statuses directly from mutmut 2.5's SQLite cache.

    mutmut 2.5.1's `mutmut results` text output only lists non-killed
    mutants (survived / suspicious / timeout / skipped). The full
    killed count is only readable from the SQLite `.mutmut-cache`
    file's `Mutant.status` column. Values we see:

      * `ok_killed`       → killed
      * `bad_survived`    → survived
      * `ok_suspicious`   → suspicious (took 2x baseline time)
      * `bad_timeout`     → timeout (took 10x, counted as killed-ish
                             under "timeout" in standard tooling but
                             we keep as a distinct bucket)
      * `skipped`         → skipped (runner rc=3; don't count)
      * `unknown`         → untested (incomplete run)

    Returns a DESIGN §3.3-shaped dict or None if the file is missing."""
    if not cache_sqlite.exists():
        return None
    try:
        import sqlite3
        con = sqlite3.connect(str(cache_sqlite))
        rows = con.execute(
            "SELECT status, COUNT(*) FROM Mutant GROUP BY status"
        ).fetchall()
        con.close()
    except Exception as e:  # noqa: BLE001
        logger.warning("sqlite read %s failed: %s", cache_sqlite, e)
        return None
    raw: dict[str, int] = {status: int(n) for status, n in rows}
    killed = raw.get("ok_killed", 0)
    survived = raw.get("bad_survived", 0)
    timeout = raw.get("bad_timeout", 0)
    suspicious = raw.get("ok_suspicious", 0)
    skipped = raw.get("skipped", 0)
    not_checked = (
        raw.get("unknown", 0) + raw.get("not_checked", 0)
    )
    reachable = killed + survived + timeout + suspicious
    total = reachable + skipped + not_checked
    score = (killed / reachable) if reachable else 0.0
    return {
        "killed": killed,
        "survived": survived,
        "timeout": timeout,
        "suspicious": suspicious,
        "skipped": skipped,
        "no_tests": 0,
        "not_checked": not_checked,
        "reachable": reachable,
        "mutant_count": total,
        "score": round(score, 4),
        "score_display": (
            f"{score * 100:.2f}%" if reachable else "n/a (reachable=0)"
        ),
        "raw_status_counts": raw,
    }


def _summarise_mutmut_output(results_txt: Path, log_file: Path) -> dict:
    """Parse the ``mutmut results`` printout into numbers.

    mutmut 2.5 prints category headers like:
        Killed 🎉 (42)
        Survived 🙁 (1255)
        Timeout ⏰ (1)
        Suspicious 🤔 (0)
        Skipped 🔇 (0)
        Untested/skipped (984)
    We extract the bracketed counts and compute DESIGN §3.3:
        reachable = killed + survived + timeout + suspicious
        score     = killed / reachable
    'Untested/skipped' mutants are treated as unreachable (mutmut's
    equivalent of 'mutant function never executed by the runner')."""
    status_counts: dict[str, int] = {}
    text = ""
    if results_txt.exists():
        text = results_txt.read_text(encoding="utf-8", errors="replace")
    if not text and log_file.exists():
        text = log_file.read_text(encoding="utf-8", errors="replace")
    # mutmut 2.x "Category (N)" header, possibly with emoji in between.
    import re
    header_re = re.compile(
        r"^(Killed|Survived|Timeout|Suspicious|Skipped|Untested[/ ]skipped)"
        r"[^\(]*\((\d+)\)",
        re.IGNORECASE | re.MULTILINE,
    )
    for m in header_re.finditer(text):
        key = m.group(1).lower().replace("/skipped", "").replace(" ", "_")
        # "Untested" alone maps to our `not_checked` bucket.
        if key == "untested":
            key = "not_checked"
        status_counts[key] = int(m.group(2))
    # Also support the mutmut 3.x `killed: 42` form for forward-compat.
    known = {
        "killed", "survived", "timeout", "suspicious",
        "skipped", "no tests", "not checked",
    }
    for line in text.splitlines():
        s = line.strip()
        if ":" not in s:
            continue
        key, _, val = s.partition(":")
        key = key.strip().lower()
        val = val.strip().split()[0] if val.strip() else ""
        if key in known and val.isdigit():
            status_counts.setdefault(key.replace(" ", "_"), int(val))
    killed = status_counts.get("killed", 0)
    survived = status_counts.get("survived", 0)
    timeout = status_counts.get("timeout", 0)
    suspicious = status_counts.get("suspicious", 0)
    skipped = status_counts.get("skipped", 0)
    no_tests = status_counts.get("no_tests", 0)
    not_checked = status_counts.get("not_checked", 0)
    reachable = killed + survived + timeout + suspicious
    total = reachable + skipped + no_tests + not_checked
    score = (killed / reachable) if reachable else 0.0
    return {
        "killed": killed,
        "survived": survived,
        "timeout": timeout,
        "suspicious": suspicious,
        "skipped": skipped,
        "no_tests": no_tests,
        "not_checked": not_checked,
        "reachable": reachable,
        "mutant_count": total,
        "score": round(score, 4),
        "score_display": (
            f"{score * 100:.2f}%" if reachable else "n/a (reachable=0)"
        ),
        "raw_status_counts": status_counts,
    }


# ---------------------------------------------------------------------------
# PIT (htsjdk) — Java bytecode mutation via pitest 1.15.3
# ---------------------------------------------------------------------------
#
# Runs PIT against htsjdk classes using the CorpusReplayTest JUnit 5 test
# that iterates every file in `biotest.corpus` through VCFFileReader or
# SamReaderFactory. The test swallows exceptions by design — PIT uses
# JaCoCo-style coverage + assertion reachability to infer kill/survive.
#
# Mutation scope matches DESIGN §3.3 + biotest_config target_filters:
#   VCF → htsjdk.variant.vcf.* + htsjdk.variant.variantcontext.*
#   SAM → htsjdk.samtools.*
# Exclusions (JEXL filter-expression engine, BCF2 binary codec) are
# handled by PIT's --excludedClasses flag so the denominator matches the
# mutmut vcfpy row's "BioTest-exercised only" principle.

PIT_DIR = REPO_ROOT / "compares" / "baselines" / "pit"
PIT_JARS = [
    PIT_DIR / "pitest-command-line-1.15.3.jar",
    PIT_DIR / "pitest-1.15.3.jar",
    PIT_DIR / "pitest-entry-1.15.3.jar",
    PIT_DIR / "pitest-junit5-plugin-1.2.1.jar",
    PIT_DIR / "commons-text-1.10.0.jar",
    PIT_DIR / "commons-lang3-3.14.0.jar",
]
JUNIT_JAR = (
    REPO_ROOT / "compares" / "baselines" / "junit"
    / "junit-platform-console-standalone-1.10.1.jar"
)
HTSJDK_FATJAR = (
    REPO_ROOT / "harnesses" / "java" / "build" / "libs"
    / "biotest-harness-all.jar"
)
PIT_TEST_CLASSES_DIR = (
    REPO_ROOT / "compares" / "harnesses" / "pit" / "classes"
)
PIT_HTSJDK_CLASSES_DIR = (
    REPO_ROOT / "compares" / "harnesses" / "pit" / "htsjdk-classes"
)
PIT_TEST_SRC_DIR = REPO_ROOT / "compares" / "harnesses" / "pit"

PIT_FORMAT_CONFIG: dict[str, tuple[str, str]] = {
    # (targetClasses, targetTests method)
    "VCF": (
        "htsjdk.variant.vcf.*,htsjdk.variant.variantcontext.*",
        "CorpusReplayTest#replayVcfCorpus",
    ),
    "SAM": (
        "htsjdk.samtools.*",
        "CorpusReplayTest#replaySamCorpus",
    ),
}
# JEXL and BCF2 are explicitly out of scope (§3.3) — mutants there would
# always survive against a pure-random VCF/SAM corpus. The NCBI SRA /
# NGS classes are also excluded: htsjdk bundles thin wrappers around
# `ngs/Alignment` which we don't redistribute, so PIT's bytecode
# analyzer trips on ClassNotFoundException.
PIT_EXCLUDED_CLASSES = (
    "*JEXL*,*Jexl*,"
    "htsjdk.variant.bcf2.*,"
    "htsjdk.variant.vcf.VCF*Encoder,"
    "htsjdk.samtools.sra.*,"
    "htsjdk.samtools.cram.*,"
    "htsjdk.samtools.SRA*"
)


def _ensure_pit_htsjdk_classes() -> None:
    """Extract htsjdk/*.class from the fatjar to a flat directory so PIT's
    coverage analyzer (which can't descend into nested jars) sees them.
    Idempotent — skips if the marker class is already on disk."""
    marker = PIT_HTSJDK_CLASSES_DIR / "htsjdk" / "variant" / "vcf" / "VCFCodec.class"
    if marker.exists():
        return
    PIT_HTSJDK_CLASSES_DIR.mkdir(parents=True, exist_ok=True)
    import zipfile
    with zipfile.ZipFile(str(HTSJDK_FATJAR)) as zf:
        for name in zf.namelist():
            if name.startswith("htsjdk/") and name.endswith(".class"):
                target = PIT_HTSJDK_CLASSES_DIR / name
                target.parent.mkdir(parents=True, exist_ok=True)
                target.write_bytes(zf.read(name))


def _run_pit_cell(
    fmt: str,
    corpus_dir: Path,
    budget_s: int,
    out_dir: Path,
) -> dict:
    """Invoke PIT against htsjdk for the given format. Returns a summary
    dict shaped like the mutmut cells: `{killed, survived, timeout,
    suspicious, not_checked, reachable, score, ...}`."""
    import xml.etree.ElementTree as _ET

    out_dir.mkdir(parents=True, exist_ok=True)
    report_dir = out_dir / "pit_report"
    if report_dir.exists():
        shutil.rmtree(report_dir, ignore_errors=True)

    target_classes, target_tests = PIT_FORMAT_CONFIG[fmt]
    # Classpath for PIT main + test + htsjdk + deps.
    cp_entries = [
        *[str(j.resolve()) for j in PIT_JARS],
        str(PIT_TEST_CLASSES_DIR.resolve()),
        str(PIT_HTSJDK_CLASSES_DIR.resolve()),
        str(JUNIT_JAR.resolve()),
    ]
    cp = os.pathsep.join(cp_entries)

    cmd = [
        "java",
        f"-Dbiotest.corpus={corpus_dir.resolve()}",
        "-cp", cp,
        "org.pitest.mutationtest.commandline.MutationCoverageReport",
        "--reportDir", str(report_dir.resolve()),
        "--targetClasses", target_classes,
        "--excludedClasses", PIT_EXCLUDED_CLASSES,
        "--targetTests", target_tests.split("#")[0],
        "--sourceDirs", str(PIT_TEST_SRC_DIR.resolve()),
        "--outputFormats", "XML",
        "--threads", "1",
        "--mutators", "DEFAULTS",
        "--timeoutConst", "8000",
        "--timeoutFactor", "1.5",
        "--verbose", "false",
    ]
    run_log = out_dir / "pit_run.log"
    start = time.time()
    with run_log.open("wb") as fh:
        try:
            rc = subprocess.run(
                cmd, stdout=fh, stderr=fh,
                timeout=budget_s, check=False,
            ).returncode
        except subprocess.TimeoutExpired:
            rc = -1
    dur = time.time() - start

    mutations_xml = report_dir / "mutations.xml"
    killed = survived = timeout = suspicious = not_checked = 0
    if mutations_xml.exists():
        try:
            tree = _ET.parse(str(mutations_xml))
            for m in tree.getroot().findall("mutation"):
                status = (m.get("status") or "").upper()
                if status == "KILLED":
                    killed += 1
                elif status == "SURVIVED":
                    survived += 1
                elif status == "TIMED_OUT":
                    timeout += 1
                elif status == "RUN_ERROR":
                    suspicious += 1
                elif status == "NO_COVERAGE":
                    # Matches mutmut's "not_checked / unreachable" bucket.
                    not_checked += 1
                elif status == "NON_VIABLE":
                    # Mutation itself was invalid — exclude from
                    # denominator.
                    not_checked += 1
                else:
                    not_checked += 1
        except Exception as e:  # noqa: BLE001
            logger.warning("parse PIT XML failed: %s", e)

    reachable = killed + survived + timeout + suspicious
    total = reachable + not_checked
    score = (killed / reachable) if reachable else 0.0
    payload = {
        "tool": "pure_random",
        "sut": f"htsjdk_{fmt.lower()}",
        "phase": "mutation",
        "engine": "PIT 1.15.3",
        "time_budget_s": budget_s,
        "killed": killed,
        "survived": survived,
        "timeout": timeout,
        "suspicious": suspicious,
        "skipped": 0,
        "no_tests": 0,
        "not_checked": not_checked,
        "reachable": reachable,
        "mutant_count": total,
        "score": round(score, 4),
        "score_display": (
            f"{score * 100:.2f}%" if reachable else "n/a (reachable=0)"
        ),
        "pit_duration_s": round(dur, 2),
        "pit_exit_code": rc,
        "corpus_dir": str(corpus_dir.resolve()),
        "target_classes": target_classes,
        "target_tests": target_tests,
        "mutations_xml": str(mutations_xml.resolve()),
    }
    (out_dir / "summary.json").write_text(
        json.dumps(payload, indent=2), encoding="utf-8",
    )
    logger.info(
        "[htsjdk_%s] PIT rc=%s dur=%.0fs killed=%d survived=%d "
        "reachable=%d score=%s",
        fmt.lower(), rc, dur, killed, survived, reachable,
        payload["score_display"],
    )
    return payload


