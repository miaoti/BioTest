def run_cell(sut: str, budget_s: int, out_root: Path) -> dict:
    """Dispatch to the per-SUT engine. Returns the per-cell summary dict."""
    out_dir = out_root / sut
    corpus_source_cell = {
        "htsjdk_vcf": "htsjdk_vcf",
        "htsjdk_sam": "htsjdk_sam",
        "vcfpy": "vcfpy",
        "noodles": "noodles",
        "biopython": "biopython",
        "seqan3": "seqan3",
    }[sut]
    try:
        corpus_dir = _union_corpus(corpus_source_cell, out_dir)
    except FileNotFoundError as e:
        logger.warning("%s — %s", sut, e)
        corpus_dir = None

    if sut == "vcfpy":
        return _run_mutmut_cell(
            sut="vcfpy",
            corpus_dir=corpus_dir,
            budget_s=budget_s,
            out_dir=out_dir,
            src_path_finder=_find_vcfpy_src,
            runner_script=VCFPY_RUNNER,
            src_env_var="MUTMUT_VCFPY_SRC",
            # Must match the actual importable package name so the
            # corpus-runner's `import vcfpy` resolves to the mutable
            # copy rather than the venv site-packages.
            mutate_dir_name="vcfpy",
        )
    if sut == "biopython":
        _ensure_biopython_runner()
        return _run_mutmut_cell(
            sut="biopython",
            corpus_dir=corpus_dir,
            budget_s=budget_s,
            out_dir=out_dir,
            src_path_finder=_find_biopython_sam_src,
            runner_script=BIOPYTHON_RUNNER,
            src_env_var="MUTMUT_BIOPYTHON_SRC",
            # single-file module — any name works; pick something
            # distinct so the biopython runner's importlib path is
            # unambiguous.
            mutate_dir_name="biopython_sam",
        )
    if sut in ("htsjdk_vcf", "htsjdk_sam"):
        fmt = "VCF" if sut == "htsjdk_vcf" else "SAM"
        reason = (
            "PIT (pitest 1.15.3 JARs downloaded to compares/baselines/pit/) "
            "requires a JUnit test harness that replays the pure_random "
            "corpus + a Maven-managed classpath. Both are pre-wired inside "
            "biotest-bench Docker image (/opt/pit/). Running the in-session "
            "Windows host would need hand-written JUnit 5 + compile step; "
            "deferred."
        )
        promote = (
            f"# Inside biotest-bench container:\n"
            f"python3.12 compares/scripts/mutation_driver.py "
            f"--tool pure_random --sut htsjdk --format {fmt} "
            f"--corpus compares/results/mutation/pure_random/{sut}/union_corpus "
            f"--budget 7200 "
            f"--out compares/results/mutation/pure_random/{sut}/"
        )
        return _run_blocked_cell(sut, "PIT (pitest 1.15.3)", reason,
                                 promote, corpus_dir, budget_s, out_dir)
    if sut == "noodles":
        reason = (
            "cargo-mutants (Rust) requires the Rust toolchain (cargo + "
            "rustc), which is absent on this Windows dev host. "
            "Dockerfile.bench bakes the toolchain and cargo-mutants is "
            "queued for install — run inside biotest-bench."
        )
        promote = (
            "# Inside biotest-bench container:\n"
            "python3.12 compares/scripts/mutation_driver.py "
            "--tool pure_random --sut noodles "
            "--corpus compares/results/mutation/pure_random/noodles/union_corpus "
            "--budget 7200 "
            "--out compares/results/mutation/pure_random/noodles/"
        )
        return _run_blocked_cell("noodles", "cargo-mutants", reason,
                                 promote, corpus_dir, budget_s, out_dir)
    if sut == "seqan3":
        reason = (
            "mull (C++ LLVM IR mutation) requires Clang 18 + mull 0.33 "
            "LLVM-18 runtime, which are only available inside "
            "biotest-bench Docker image (DESIGN §13.2.4 + §13.3.3). Not "
            "reproducible on the Windows/MSYS2 host."
        )
        promote = (
            "# Inside biotest-bench container:\n"
            "python3.12 compares/scripts/mutation_driver.py "
            "--tool pure_random --sut seqan3 "
            "--corpus compares/results/mutation/pure_random/seqan3/union_corpus "
            "--budget 7200 "
            "--out compares/results/mutation/pure_random/seqan3/"
        )
        return _run_blocked_cell("seqan3", "mull 0.33 (LLVM 18)",
                                 reason, promote, corpus_dir, budget_s, out_dir)
    raise ValueError(f"unsupported sut: {sut!r}")


MATRIX = (
    "htsjdk_vcf", "htsjdk_sam", "vcfpy", "noodles", "biopython", "seqan3",
)


def _write_summary_csv(rows: list[dict], out: Path) -> None:
    cols = [
        "sut", "engine", "time_budget_s", "killed", "survived",
        "timeout", "suspicious", "skipped", "no_tests", "not_checked",
        "reachable", "mutant_count", "score", "score_display",
        "blocked_reason",
    ]
    out.parent.mkdir(parents=True, exist_ok=True)
    with out.open("w", newline="", encoding="utf-8") as fh:
        w = csv.DictWriter(fh, fieldnames=cols, extrasaction="ignore")
        w.writeheader()
        for r in rows:
            w.writerow({k: r.get(k, "") for k in cols})


def _cli() -> int:
    p = argparse.ArgumentParser(description=__doc__.splitlines()[0])
    p.add_argument("--sut", choices=list(MATRIX),
                   help="single SUT cell (or use --run-all)")
    p.add_argument("--budget", type=int, default=7200)
    p.add_argument("--out", type=Path, default=None,
                   help="override cell output dir (single-cell only)")
    p.add_argument("--run-all", action="store_true",
                   help="iterate all 6 DESIGN §13.5 Phase-3 Pure Random cells")
    p.add_argument("--verbose", action="store_true")
    args = p.parse_args()

    logging.basicConfig(
        level=logging.DEBUG if args.verbose else logging.INFO,
        format="%(asctime)s %(levelname)s %(name)s %(message)s",
    )

    if not args.run_all and not args.sut:
        print("Need --run-all or --sut <name>", file=sys.stderr)
        return 2

    cells = list(MATRIX) if args.run_all else [args.sut]
    rows: list[dict] = []
    for sut in cells:
        out_root = MUTATION_ROOT if args.out is None else args.out.parent
        try:
            stats = run_cell(sut, args.budget, out_root)
        except Exception as e:  # noqa: BLE001
            logger.exception("cell %s failed", sut)
            stats = {
                "tool": "pure_random", "sut": sut, "phase": "mutation",
                "killed": 0, "reachable": 0, "score": 0.0,
                "score_display": "error", "blocked_reason": repr(e),
            }
        rows.append(stats)

    _write_summary_csv(rows, MUTATION_ROOT / "summary.csv")
    print()
    print(f"{'sut':<14}{'engine':<22}{'killed':>8}{'reachable':>12}"
          f"{'score':>10}  {'status':<30}")
    print("-" * 100)
    for r in rows:
        eng = r.get("engine", "mutmut 3.x") if not r.get("blocked_reason") else r.get("engine", "blocked")
        status = r.get("blocked_reason", "ok")[:30]
        print(f"{r['sut']:<14}{eng:<22}"
              f"{r.get('killed',0):>8}{r.get('reachable',0):>12}"
              f"{(r.get('score_display','') or 'n/a'):>10}  {status:<30}")
    return 0


if __name__ == "__main__":
    raise SystemExit(_cli())
