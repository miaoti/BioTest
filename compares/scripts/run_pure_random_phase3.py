def _copy_source_tree(module_init_or_file: Path, dst: Path) -> Path:
    """Copy an installed Python package/module into a mutable tree.

    * If `module_init_or_file` is a directory (package __init__.py's
      parent), copy the whole package.
    * If it's a single module file (Bio/Align/sam.py), copy the file
      alone under dst.
    Returns the path mutmut should treat as `paths_to_mutate`.
    """
    dst.parent.mkdir(parents=True, exist_ok=True)
    if dst.exists():
        if dst.is_dir():
            shutil.rmtree(dst)
        else:
            dst.unlink()
    if module_init_or_file.is_dir():
        shutil.copytree(module_init_or_file, dst)
        return dst
    # single file — create the wrapping dir
    dst.mkdir(parents=True)
    out_file = dst / module_init_or_file.name
    shutil.copy2(module_init_or_file, out_file)
    return out_file


def _find_vcfpy_src() -> Path:
    import vcfpy
    return Path(vcfpy.__file__).parent


def _find_biopython_sam_src() -> Path:
    from Bio.Align import sam
    return Path(sam.__file__)


BIOPYTHON_RUNNER_SRC = r'''"""Biopython Bio.Align.sam corpus-runner for mutmut (Phase-3 driver).

mutmut contract (``[tool.mutmut] runner = "<cmd>"``):
  * exit 0 → SURVIVED; exit 1 → KILLED; exit 33 → no tests.

This script plays the `pytest` role: replays a pure_random corpus
through the mutated `Bio.Align.sam.AlignmentIterator` and compares
fingerprints to a pre-mutation baseline.

Envs (set by mutation_driver):
  * MUTMUT_RUNNER_MODE        "baseline" or "check" (default "check")
  * MUTMUT_BIOPYTHON_SRC      path to mutable Bio/Align/sam.py
  * MUTMUT_CORPUS_DIR         directory of corpus files
  * MUTMUT_BASELINE_FILE      JSON {filename: fingerprint}
  * MUTMUT_CORPUS_SAMPLE      optional N (default 200)
  * MUTMUT_CORPUS_TIMEOUT_S   optional per-file timeout (default 2.0s)
"""

from __future__ import annotations

import hashlib
import json
import os
import signal
import sys
from pathlib import Path


def _ensure_src_on_path() -> None:
    src = os.environ.get("MUTMUT_BIOPYTHON_SRC")
    if not src:
        return
    pkg_file = Path(src).resolve()
    # If mutmut mutates Bio/Align/sam.py in place we need its parent
    # three levels up on sys.path so `from Bio.Align import sam` loads
    # our mutable copy. But dragging the whole Bio tree into the copy
    # is wasteful — instead, we replace the LIVE module's bytecode by
    # importing from the mutable file directly.
    import importlib.util
    spec = importlib.util.spec_from_file_location(
        "Bio.Align.sam", str(pkg_file),
    )
    if spec is None or spec.loader is None:
        return
    module = importlib.util.module_from_spec(spec)
    # Pre-import heavy deps so numpy's C ext loads before our swap.
    import numpy  # noqa: F401
    from Bio import Align  # noqa: F401 — parent package
    spec.loader.exec_module(module)
    sys.modules["Bio.Align.sam"] = module


class _TimeoutError(Exception):
    pass


def _alarm_handler(_signum, _frame):
    raise _TimeoutError("per-file parse exceeded MUTMUT_CORPUS_TIMEOUT_S")


def _sample_corpus_files(corpus_dir: Path, max_n: int) -> list[Path]:
    return sorted(p for p in corpus_dir.iterdir() if p.is_file())[:max_n]


def _fingerprint_for_file(path: Path, timeout_s: float) -> str:
    from Bio.Align import sam as _sam

    h = hashlib.sha1()
    if os.name == "posix":
        signal.signal(signal.SIGALRM, _alarm_handler)
        signal.setitimer(signal.ITIMER_REAL, timeout_s)
    try:
        try:
            with open(path) as fh:
                it = _sam.AlignmentIterator(fh)
                count = 0
                for rec in it:
                    count += 1
                    if count >= 200:
                        h.update(b"|cap")
                        break
                    try:
                        row = (
                            getattr(rec, "target", None),
                            getattr(rec, "query", None),
                            type(rec).__name__,
                        )
                        h.update(b"|r")
                        h.update(repr(row).encode(errors="replace"))
                    except BaseException as exc:
                        h.update(b"|r-fail:")
                        h.update(type(exc).__name__.encode())
                h.update(f"|n={count}".encode())
        except BaseException as exc:
            h.update(f"open-or-parse-fail:{type(exc).__name__}".encode())
    except _TimeoutError:
        return hashlib.sha1(b"timeout").hexdigest()
    finally:
        if os.name == "posix":
            signal.setitimer(signal.ITIMER_REAL, 0)
    return h.hexdigest()


def _run_baseline(corpus: Path, baseline: Path, n: int, to: float) -> int:
    _ensure_src_on_path()
    out = {p.name: _fingerprint_for_file(p, to)
           for p in _sample_corpus_files(corpus, n)}
    baseline.parent.mkdir(parents=True, exist_ok=True)
    baseline.write_text(json.dumps(out, indent=2), encoding="utf-8")
    print(f"[baseline] wrote {len(out)} entries to {baseline}")
    return 0


def _run_check(corpus: Path, baseline: Path, n: int, to: float) -> int:
    if not baseline.exists():
        print(f"[check] baseline missing: {baseline}", file=sys.stderr)
        return 33
    ref = json.loads(baseline.read_text(encoding="utf-8"))
    _ensure_src_on_path()
    for p in _sample_corpus_files(corpus, n):
        if p.name not in ref:
            continue
        fp = _fingerprint_for_file(p, to)
        if fp != ref[p.name]:
            print(f"[check] KILL {p.name}: base={ref[p.name][:12]} "
                  f"mut={fp[:12]}", file=sys.stderr)
            return 1
    return 0


def main() -> int:
    mode = os.environ.get("MUTMUT_RUNNER_MODE", "check").lower()
    corpus = Path(os.environ.get("MUTMUT_CORPUS_DIR", "")).resolve()
    baseline = Path(os.environ.get("MUTMUT_BASELINE_FILE", "")).resolve()
    n = int(os.environ.get("MUTMUT_CORPUS_SAMPLE", "200"))
    to = float(os.environ.get("MUTMUT_CORPUS_TIMEOUT_S", "2.0"))
    if not corpus.exists() or not corpus.is_dir():
        print(f"[runner] MUTMUT_CORPUS_DIR invalid: {corpus}", file=sys.stderr)
        return 2
    if mode == "baseline":
        return _run_baseline(corpus, baseline, n, to)
    return _run_check(corpus, baseline, n, to)


if __name__ == "__main__":
    raise SystemExit(main())
'''


def _ensure_biopython_runner() -> Path:
    """Materialise the biopython corpus-runner next to the vcfpy one."""
    BIOPYTHON_RUNNER.parent.mkdir(parents=True, exist_ok=True)
    if not BIOPYTHON_RUNNER.exists():
        BIOPYTHON_RUNNER.write_text(BIOPYTHON_RUNNER_SRC, encoding="utf-8")
    return BIOPYTHON_RUNNER


def _run_mutmut_cell(
    sut: str,
    corpus_dir: Path,
    budget_s: int,
    out_dir: Path,
    src_path_finder,  # Callable[[], Path]
    runner_script: Path,
    src_env_var: str,
    mutate_dir_name: str,
) -> dict:
    """Shared mutmut cell for vcfpy / biopython."""
    out_dir.mkdir(parents=True, exist_ok=True)
    baseline_file = out_dir / "baseline.json"
    runner_log = out_dir / "runner.log"
    mutmut_log = out_dir / "mutmut_run.log"
    results_txt = out_dir / "mutmut_results.txt"
    summary_path = out_dir / "summary.json"

    # Materialise a mutable copy of the SUT source under `src/` — mutmut
    # 3.0.0's guess_paths_to_mutate() falls back to `src/` or
    # `<cwd_basename>/` when setup.cfg lacks `paths_to_mutate`. Using
    # `src/` is the cleanest cross-cell convention.
    src_src = src_path_finder()
    src_dst = _copy_source_tree(src_src, out_dir / "src" / mutate_dir_name)

    # 1. Capture baseline fingerprints from the unmutated SUT.
    logger.info("[%s] capturing baseline → %s", sut, baseline_file)
    env = os.environ.copy()
    env.update({
        "MUTMUT_RUNNER_MODE": "baseline",
        src_env_var: str(src_dst.resolve()),
        "MUTMUT_CORPUS_DIR": str(corpus_dir.resolve()),
        "MUTMUT_BASELINE_FILE": str(baseline_file.resolve()),
        "MUTMUT_CORPUS_SAMPLE": "200",
        "MUTMUT_CORPUS_TIMEOUT_S": "2.0",
        "PYTHONUNBUFFERED": "1",
    })
    with runner_log.open("wb") as fh:
        rc = subprocess.run(
            [sys.executable, str(runner_script)],
            env=env, stdout=fh, stderr=fh,
            check=False, timeout=600,
        ).returncode
    if rc != 0 or not baseline_file.exists():
        return {
            "skipped": True,
            "blocked_reason": (
                f"baseline capture failed for {sut} (rc={rc}); "
                f"see {runner_log}"
            ),
        }
    baseline_entries = len(json.loads(
        baseline_file.read_text(encoding="utf-8"),
    ))
    logger.info("[%s] baseline entries: %d", sut, baseline_entries)

    # 2. mutmut 2.5 refuses to start unless a `tests/` dir exists (even
    #    though we pass a custom --runner that doesn't use pytest). Put
    #    a marker file so the directory scan succeeds; the runner ignores
    #    anything in tests/.
    (out_dir / "tests").mkdir(exist_ok=True)
    (out_dir / "tests" / ".keep").touch()

    # 3. Invoke `mutmut run` with CLI flags (mutmut 2.5.1 syntax).
    #    `--paths-to-mutate` points at our mutable source; `--runner`
    #    is the per-mutant test suite (our fingerprint-compare driver).
    target_rel = f"src/{mutate_dir_name}"
    # mutmut forwards `--runner` to shell execution, so it must be a
    # single string; use forward-slashes for Windows friendliness.
    runner_cmd = (
        f'"{sys.executable}" "{runner_script.resolve()}"'
    )
    env["MUTMUT_RUNNER_MODE"] = "check"
    # mutmut's progress renderer uses Braille spinner chars that cp1252
    # can't encode; force UTF-8 on the subprocess so the ASCII fallback
    # doesn't kick in mid-run with a UnicodeEncodeError.
    env["PYTHONIOENCODING"] = "utf-8"
    # Clean any prior .mutmut-cache so runs are deterministic (mutmut
    # 2.5.1 keeps cache in cwd; would otherwise resume partially-run
    # cells across script invocations).
    cache_dir = out_dir / ".mutmut-cache"
    if cache_dir.exists():
        shutil.rmtree(cache_dir, ignore_errors=True)
    start = time.time()
    with mutmut_log.open("wb") as fh:
        try:
            rc = subprocess.run(
                [
                    sys.executable, "-m", "mutmut", "run",
                    "--paths-to-mutate", target_rel,
                    "--runner", runner_cmd,
                    # `--CI` uniform exit codes; easier to parse.
                    "--CI",
                    # Quieter output — no Braille spinner.
                    "--simple-output",
                ],
                env=env, cwd=str(out_dir), stdout=fh, stderr=fh,
                check=False, timeout=budget_s,
            ).returncode
        except subprocess.TimeoutExpired:
            rc = -1
    dur = time.time() - start
    logger.info("[%s] mutmut run rc=%s in %.0fs", sut, rc, dur)

    # 4. `mutmut results` summary.
    with results_txt.open("wb") as fh:
        subprocess.run(
            [sys.executable, "-m", "mutmut", "results"],
            env=env, cwd=str(out_dir), stdout=fh, stderr=fh,
            check=False, timeout=120,
        )

    # 5. Extract counts directly from mutmut 2.5's cache if present
    #    (more reliable than scraping stdout on cp1252-hobbled Windows).
    cache_db = out_dir / ".mutmut-cache"
    if cache_db.exists():
        # Copy for audit; leave in place for `mutmut results`.
        shutil.copytree(
            cache_db, out_dir / "mutmut_cache_backup", dirs_exist_ok=True,
        )

    stats = _summarise_mutmut_output(results_txt, mutmut_log)
    stats.update({
        "tool": "pure_random",
        "sut": sut,
        "phase": "mutation",
        "time_budget_s": budget_s,
        "mutmut_exit_code": rc,
        "mutmut_duration_s": round(dur, 2),
        "baseline_entries": baseline_entries,
        "paths_to_mutate": [target_rel],
        "corpus_dir": str(corpus_dir),
    })
    summary_path.write_text(json.dumps(stats, indent=2), encoding="utf-8")
    logger.info("[%s] wrote %s", sut, summary_path)
    return stats


def _summarise_mutmut_output(results_txt: Path, log_file: Path) -> dict:
    """Parse mutmut 3.x's `mutmut results` printout into numbers.

    mutmut prints lines like ``killed: 42 🎉`` / ``survived: 13 🙁`` /
    ``timeout: 1 ⏰`` etc. Extract the counts and compute the
    DESIGN §3.3 score = killed / reachable (reachable = killed +
    survived + timeout + suspicious)."""
    status_counts: dict[str, int] = {}
    text = ""
    if results_txt.exists():
        text = results_txt.read_text(encoding="utf-8", errors="replace")
    if not text and log_file.exists():
        text = log_file.read_text(encoding="utf-8", errors="replace")
    known = {
        "killed", "survived", "timeout", "suspicious",
        "skipped", "no tests", "not checked",
    }
    for line in text.splitlines():
        s = line.strip()
        if ":" not in s:
            continue
        key, _, val = s.partition(":")
        key = key.strip().lower()
        val = val.strip().split()[0] if val.strip() else ""
        if key in known and val.isdigit():
            status_counts[key.replace(" ", "_")] = int(val)
    killed = status_counts.get("killed", 0)
    survived = status_counts.get("survived", 0)
    timeout = status_counts.get("timeout", 0)
    suspicious = status_counts.get("suspicious", 0)
    skipped = status_counts.get("skipped", 0)
    no_tests = status_counts.get("no_tests", 0)
    not_checked = status_counts.get("not_checked", 0)
    reachable = killed + survived + timeout + suspicious
    total = reachable + skipped + no_tests + not_checked
    score = (killed / reachable) if reachable else 0.0
    return {
        "killed": killed,
        "survived": survived,
        "timeout": timeout,
        "suspicious": suspicious,
        "skipped": skipped,
        "no_tests": no_tests,
        "not_checked": not_checked,
        "reachable": reachable,
        "mutant_count": total,
        "score": round(score, 4),
        "score_display": (
            f"{score * 100:.2f}%" if reachable else "n/a (reachable=0)"
        ),
        "raw_status_counts": status_counts,
    }


