"""Phase 3 — Mutation score, Pure Random × every SUT driver.

Covers DESIGN.md §13.5 Phase 3 checkbox "Pure Random × every SUT
(6 cells; one PIT + two mutmut + two mull-or-cargo-mutants calls.
The floor-baseline's mutation score is the key comparator for the
other tools)". Emits per-cell `summary.json` files matching the
DESIGN.md §3.3 contract
(``{killed, reachable, score, mutator_count, mutant_count}``)
alongside a matrix-level summary.csv so the Phase-6 report
consumer reads a uniform shape.

Test-kill protocol (DESIGN §3.3) — per cell:

  1. Corpus = Pure Random's Phase-2 generated files (200 random
     files per rep × 3 reps union). Feeds the mutation tool as
     "test suite".
  2. Baseline: parse each file with the unmutated SUT, hash the
     record stream → {filename: fingerprint} JSON.
  3. Mutator loop: for each mutant generated by the per-language
     engine, re-run the corpus through the mutated SUT. If any
     fingerprint flips, the mutant is KILLED; otherwise SURVIVED.
  4. score = killed / reachable; reachable = killed + survived +
     timeout + suspicious (mutmut's standard definition — any
     mutant whose code was actually executed by the corpus).
  5. Emit `summary.json` and append a row to the matrix CSV.

Per-SUT mutation engine dispatch:

   | SUT         | Engine          | Notes                                 |
   |:------------|:----------------|:--------------------------------------|
   | htsjdk      | PIT             | Deferred — see DESIGN §13.5 Phase 3   |
   | vcfpy       | mutmut 3.x      | Uses vcfpy_corpus_runner.py fingerprint runner |
   | biopython   | mutmut 3.x      | Uses biopython_corpus_runner.py (new) |
   | seqan3      | mull            | Deferred — Clang 18 + mull on Linux only |
   | noodles-vcf | cargo-mutants   | Deferred — Rust toolchain absent on this host |

On the Windows dev host mutmut runs natively (installed via
``pip install mutmut==3.0.0``); PIT / mull / cargo-mutants are
NOT in scope here and each produces a blocked-matrix-slot entry
with a ``blocked_reason`` and the promotion command to re-run
inside ``biotest-bench:latest``. This mirrors the Phase-2
pure_random noodles-vcf handling — every cell is represented so
Phase-6 CI bands compare fairly across tools.

Usage:

    py -3.12 compares/scripts/run_pure_random_phase3.py \\
        --run-all --budget 7200

Or per-cell:

    py -3.12 compares/scripts/run_pure_random_phase3.py \\
        --sut vcfpy --budget 7200 \\
        --out compares/results/mutation/pure_random/vcfpy

Requires that Phase 2 has already run (reads the corpus from
``compares/results/coverage/pure_random/<cell>/run_*/corpus``).
"""

