def _run_blocked_cell(
    sut: str,
    engine: str,
    reason: str,
    promote_cmd: str,
    corpus_dir: Path | None,
    budget_s: int,
    out_dir: Path,
) -> dict:
    out_dir.mkdir(parents=True, exist_ok=True)
    payload = {
        "tool": "pure_random",
        "sut": sut,
        "phase": "mutation",
        "engine": engine,
        "time_budget_s": budget_s,
        "killed": 0,
        "survived": 0,
        "timeout": 0,
        "suspicious": 0,
        "skipped": 0,
        "no_tests": 0,
        "not_checked": 0,
        "reachable": 0,
        "mutant_count": 0,
        "score": 0.0,
        "score_display": "blocked",
        "blocked_reason": reason,
        "promotion_command": promote_cmd,
        "note": (
            "mutation_growth fields are 0 for this cell — see "
            "blocked_reason. Matrix slot preserved so Phase-6 "
            "comparisons stay complete across tools; rerun inside "
            "biotest-bench Docker image to promote to real numbers."
        ),
        "corpus_dir": str(corpus_dir) if corpus_dir else "",
    }
    (out_dir / "summary.json").write_text(
        json.dumps(payload, indent=2), encoding="utf-8",
    )
    logger.info("[%s] blocked: %s", sut, reason[:80])
    return payload


