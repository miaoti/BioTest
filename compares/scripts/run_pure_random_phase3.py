def _union_corpus(cell_name: str, out: Path) -> Path:
    """Merge the 3 Phase-2 reps' `run_*/corpus` directories under one
    tree so mutmut's corpus-runner sees a single stable root. Filename
    collisions resolve by keeping the first occurrence — with
    pure_random's deterministic RNG seeding (rep_idx → `0xB10 + i`),
    rep 0 already owns a unique prefix so the union is well-defined."""
    src = COVERAGE_ROOT / cell_name
    rep_dirs = sorted(src.glob("run_*/corpus"))
    if not rep_dirs:
        raise FileNotFoundError(
            f"Phase 2 corpus for cell {cell_name!r} not found "
            f"under {src}. Run Phase 2 first via "
            "`py -3.12 compares/scripts/run_pure_random_phase2.py "
            f"--run-all`."
        )
    dst = out / "union_corpus"
    dst.mkdir(parents=True, exist_ok=True)
    count = 0
    # Rename per-rep so collisions don't clobber content.
    for rep in rep_dirs:
        rep_label = rep.parent.name  # e.g. "run_0"
        for p in sorted(rep.iterdir()):
            if not p.is_file():
                continue
            tgt = dst / f"{rep_label}_{p.name}"
            if not tgt.exists():
                shutil.copy2(p, tgt)
                count += 1
    logger.info("union corpus for %s: %d files → %s", cell_name, count, dst)
    return dst


