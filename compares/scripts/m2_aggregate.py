"""Aggregate M2 dual-policy results into a single Δ_cmin table.

Reads:
  compares/results/m2_mut/<cell>/rep_<r>/{kf,of}/summary.json
  compares/results/m2_cmin/<cell>/rep_<r>/{kf,of}_summary.json
  compares/results/mutation/biotest_rep_<r>/<sut>/summary.json   (run-10 ref)

Writes:
  compares/results/m2_cmin/AGGREGATE.json
  documents/paper/fix/M2.md

Cells:
  - vcfpy_biotest      → engine = mutmut, ref = biotest_rep_<r>/vcfpy
  - biopython_biotest  → engine = atheris-mutmut, ref = biotest_rep_<r>/biopython
  - biopython_atheris  → engine = atheris-mutmut, ref = atheris/biopython/run_<r>
"""
from __future__ import annotations

import json
import math
import statistics as stats
from pathlib import Path

REPO = Path(__file__).resolve().parents[2]


def _read_summary(p: Path) -> dict | None:
    try:
        return json.loads(p.read_text(encoding="utf-8"))
    except (FileNotFoundError, json.JSONDecodeError):
        return None


def _score(sj: dict | None) -> tuple[int, int, float] | None:
    """Extract (killed, reachable, score) from a summary.json (handles both
    mutmut and atheris-mutmut layouts)."""
    if not sj:
        return None
    ms = sj.get("mutation_score")
    if isinstance(ms, dict) and "killed" in ms and "reachable" in ms:
        k, r = ms["killed"], ms["reachable"]
    else:
        k, r = sj.get("killed"), sj.get("reachable")
    if k is None or r is None or r == 0:
        return None
    return int(k), int(r), float(k) / r


def _agg(cell_results: dict[int, tuple[int, int, float]]) -> dict:
    if not cell_results:
        return {"reps": 0, "score_mean_pp": None, "score_std_pp": None,
                "killed_mean": None, "reach_mean": None}
    scores = [v[2] for v in cell_results.values()]
    killed = [v[0] for v in cell_results.values()]
    reach = [v[1] for v in cell_results.values()]
    return {
        "reps": len(scores),
        "score_mean_pp": round(100 * stats.mean(scores), 2),
        "score_std_pp": round(100 * (stats.pstdev(scores) if len(scores) > 1 else 0.0), 2),
        "killed_mean": round(stats.mean(killed), 1),
        "reach_mean": round(stats.mean(reach), 1),
    }


def collect() -> dict:
    out: dict = {}
    cells = {
        "vcfpy_biotest":     ("vcfpy",     "biotest_rep_{r}/vcfpy"),
        "biopython_biotest": ("biopython", "biotest_rep_{r}/biopython"),
        "biopython_atheris": ("biopython", "atheris/biopython"),  # has run_<r> subdirs
    }
    for cell, (sut, ref_template) in cells.items():
        out[cell] = {"sut": sut}
        for strat in ("kf", "of"):
            results: dict[int, tuple[int, int, float]] = {}
            cmin_summary = {}
            for r in range(4):
                p_mut = REPO / f"compares/results/m2_mut/{cell}/rep_{r}/{strat}/summary.json"
                p_cmin = REPO / f"compares/results/m2_cmin/{cell}/rep_{r}/{strat}_summary.json"
                sj = _read_summary(p_mut)
                sc = _score(sj)
                if sc:
                    results[r] = sc
                cs = _read_summary(p_cmin)
                if cs:
                    cmin_summary[r] = cs
            out[cell][strat] = {
                "per_rep": {str(k): {"killed": v[0], "reach": v[1],
                                      "score_pp": round(100 * v[2], 2)}
                             for k, v in results.items()},
                "agg": _agg(results),
                "cmin_summary": cmin_summary,
            }
        # Reference (existing run-10 / current headline) for sanity.
        ref_results: dict[int, tuple[int, int, float]] = {}
        for r in range(4):
            ref_path = REPO / f"compares/results/mutation/{ref_template.format(r=r)}/summary.json"
            sj = _read_summary(ref_path)
            sc = _score(sj)
            if sc:
                ref_results[r] = sc
        out[cell]["ref"] = {
            "per_rep": {str(k): {"killed": v[0], "reach": v[1],
                                  "score_pp": round(100 * v[2], 2)}
                         for k, v in ref_results.items()},
            "agg": _agg(ref_results),
        }
        kf = out[cell]["kf"]["agg"]; of = out[cell]["of"]["agg"]
        if kf["score_mean_pp"] is not None and of["score_mean_pp"] is not None:
            out[cell]["delta_cmin_pp"] = round(kf["score_mean_pp"] - of["score_mean_pp"], 2)
        else:
            out[cell]["delta_cmin_pp"] = None
    return out


